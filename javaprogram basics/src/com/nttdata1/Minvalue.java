package com.nttdata1;

public class Minvalue {

	public static void main(String[] args)
	{
		int intvalue = Integer.MIN_VALUE;
		float floatvalue = Float.MIN_VALUE;
		byte bytevalue = Byte.MIN_VALUE;
		short shortvalue = Short.MIN_VALUE;
		long longvalue = Long.MIN_VALUE;
		double doublevalue = Double.MIN_VALUE;
		boolean boolvalue = Boolean.TRUE;
		char c='d';
		System.out.println("value of the integer is min"+ intvalue);
		System.out.println("value of the float is min"+ floatvalue);
		System.out.println("value of the byte is min"+ bytevalue);
		System.out.println("value of the short is min"+ shortvalue);
		System.out.println("value of the long is min"+ longvalue);
		System.out.println("value of the double is min"+ doublevalue);
		System.out.println("value of the boolean is "+ boolvalue);
		System.out.println("value of the char is"+ c);
	}
}
